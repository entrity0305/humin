def 피보나치(숫자):

    if 숫자==1 or 숫자==0:

        return 1


    else:

        return 피보나치(숫자-1)+피보나치(숫자-2)



변수 = 0

while 변수<10:
    print(피보나치(변수))

    변수 = 변수+1

